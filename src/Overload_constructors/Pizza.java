package Overload_constructors;

public class Pizza {
//in order to prepare a pizza:
	String bread;
	String sauce;
	String cheese;
	String topping;
	
	
	Pizza(String bread,String sauce,String cheese,String topping)	{			//parameters
		
		this.bread=bread;
		this.sauce=sauce;
		this.cheese=cheese;
		this.topping=topping;
		
	}
}
